package com.test.homework.while_loop;

public class SSquare {
    /*
    print to console a square 10*10 with 'S'
    using loop while
     */
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.println("SSSSSSSSSS");
            i++;
        }
    }
}